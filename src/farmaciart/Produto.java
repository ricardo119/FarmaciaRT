

package farmaciart;


public class Produto {
    
    String nome;
    String descricao;
    int stock;
    double preco;
    double iva;
    
    
    public Produto(String nome , String descricao, int stock , double preco , double iva){
        
        this.nome = nome;
        this.descricao = descricao;
        this.stock = stock;
        this.preco = preco;
        this.iva = iva;
        
    }
    
    }
    

